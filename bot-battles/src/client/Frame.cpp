#include "Frame.h"

namespace sand {

//----------------------------------------------------------------------------------------------------
Frame::Frame()
    : m_frame(0)
    , m_timestamp(0.0f)
{
}

//----------------------------------------------------------------------------------------------------
Frame::Frame(U32 frame, F32 timestamp)
    : m_frame(frame)
    , m_timestamp(timestamp)
{
}

//----------------------------------------------------------------------------------------------------
U32 Frame::GetFrame() const
{
    return m_frame;
}

//----------------------------------------------------------------------------------------------------
F32 Frame::GetTimestamp() const
{
    return m_timestamp;
}
}
